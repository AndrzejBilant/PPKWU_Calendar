package Calendar;

class Event
{
    String link;
    String date;
    String text;
    String year;
    String month;

    public Event(String link, String date, String text, String year, String month) {
        this.link = link;
        this.date = date;
        this.text = text;
        this.year = year;
        this.month = month;
    }
}
